# 求1+2-3+4-5+6...99的和

s1 = 0  # 定义接收所有偶数的变量
s2 = 0  # 定义接收所有奇数的变量
for count in range(1, 100):  # 取1-99的所有数字，顾头不顾尾
    # print(count)
    if count % 2 == 0:  # 取1-99的所有偶数
        s1 = s1 + count  # 将1-99所有偶数相加
    else:  # 取1-99所有奇数
        s2 = s2 - count  # 计算1-99所有奇数的值
    s = s1 + s2  # 把所有偶数的值和奇数的值进行相加
    print(s)  # 
    print("Hello")